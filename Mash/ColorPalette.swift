import Foundation
import UIKit

extension UIColor {
    
    static func myBlueColor() -> UIColor {
        return UIColor(red: 0.365, green: 1.000, blue: 1.000, alpha: 1.00)
    }
    
    static func myGreenColor() -> UIColor {
        return UIColor(red: 0.063, green: 0.329, blue: 0.090, alpha: 1.00)
    }
    
    static func myPurpleColor() -> UIColor {
        return UIColor(red: 0.777, green: 0.431, blue: 0.941, alpha: 1.00)
    }
    
    static func myYellowColor() -> UIColor {
        return UIColor(red: 0.980, green: 0.871, blue: 0.135, alpha: 1.00)
    }
    
    static func myOrangeColor() -> UIColor {
        return UIColor(red: 1.000, green: 0.505, blue: 0.000, alpha: 1.00)
    }
    
    static func myGrayColor() -> UIColor {
        return UIColor(red: 0.290, green: 0.290, blue: 0.290, alpha: 1.00)
    }
}